<p>Każdy zakup pakietu skutkuje automatycznym wystawieniem faktury VAT:</p>
<ol>
    <li>Po opłaceniu (PayU IPN) tworzona jest FV z numerem <code>FV/RRRR/MM/####</code> (np. FV/2026/06/0001).</li>
    <li>FV jest wysyłana do <strong>KSeF</strong> (Krajowy System e-Faktur Ministerstwa Finansów).</li>
    <li>PDF do pobrania znajdziesz w panelu w sekcji <strong>„Faktury"</strong>.</li>
</ol>
<h3>Faktura B2C (osoba fizyczna)</h3>
<p>Domyślnie wystawiamy FV imienną — z Twoim imieniem, nazwiskiem i adresem podanymi podczas checkoutu.</p>
<h3>Faktura B2B (firma)</h3>
<p>W formularzu checkout zaznacz <em>„Kupuję na firmę"</em> i podaj NIP. Walidujemy poprawność algorytmem MF i wysyłamy FV z danymi firmy.</p>

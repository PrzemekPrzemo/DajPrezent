<p>Aby zarezerwować prezent z czyjejś listy:</p>
<ol>
    <li>Wejdź na link otrzymany od właściciela (np. <code>dajprezent.pl/anna-30</code>).</li>
    <li>Kliknij przycisk <strong>„Zarezerwuj prezent"</strong> przy wybranej pozycji.</li>
    <li>Podaj swój adres e-mail. Otrzymasz link aktywacyjny (anty-spam).</li>
    <li>Kliknij w link z maila — gotowe, prezent jest oznaczony jako zarezerwowany.</li>
</ol>
<p>Właściciel listy nigdy nie zobaczy Twojego e-maila — wymagamy go tylko po to, żeby ktoś nie spamował rezerwacji.</p>

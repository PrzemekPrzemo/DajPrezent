<p>Ochrona hasłem dostępna jest w pakietach <strong>Plus</strong> i wyższych (Pro + Wedding).</p>
<ol>
    <li>W panelu otwórz Ustawienia listy.</li>
    <li>W sekcji „Hasło dostępu" wpisz minimum 4 znaki i zapisz.</li>
    <li>Od tej chwili każdy odwiedzający stronę listy musi wpisać hasło.</li>
</ol>
<p>Hasło jest hashowane (bcrypt), nigdy nie przechowujemy go w postaci czytelnej.</p>
<p>Aby zdjąć ochronę, w tych samych ustawieniach zaznacz <em>„Usuń hasło"</em>.</p>
<p>Próby wpisania błędnego hasła są limitowane do 5 na minutę z jednego IP — bezpieczeństwo przed brute-force.</p>

<p>W panelu, na karcie listy, kliknij przycisk <strong>„Udostępnij listę"</strong>. Rozwinie się sekcja z:</p>
<ul>
    <li>📋 <strong>Kopiuj link</strong> — gotowy URL do wklejenia gdziekolwiek.</li>
    <li>💬 <strong>WhatsApp</strong> — natychmiastowy share z gotowym tekstem.</li>
    <li>📨 <strong>Messenger</strong> — share przez Facebook.</li>
    <li>✉️ <strong>E-mail</strong> — otwiera kompozytor maila z tekstem i linkiem.</li>
    <li>⬇ <strong>QR (SVG)</strong> — pobierzesz i wydrukujesz na zaproszeniu albo wrzucisz na lodówkę.</li>
</ul>
<p>Każdy gość wchodzi na Twój link — nie potrzebuje konta, nic nie instaluje.</p>

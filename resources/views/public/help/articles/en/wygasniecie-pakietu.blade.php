<p>Standard plans are valid for <strong>9 months</strong>. The wedding plan — <strong>12 months</strong> (renewable).</p>
<p>What happens after expiry:</p>
<ul>
    <li>The list page at <code>dajprezent.pl/&lt;slug&gt;</code> stops being publicly available (shows "List expired").</li>
    <li>Your data (gifts, photos, reservations) is <strong>kept for 30 days</strong>.</li>
    <li>During that time you can renew the plan — everything comes back without any losses.</li>
    <li>After 30 days gifts and reservations are deleted (GDPR PII purge), invoices are kept for 5 years (Polish accounting law).</li>
</ul>
<p>14 days before expiry the dashboard shows a red warning chip "expires in X days".</p>

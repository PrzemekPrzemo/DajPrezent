<p>Three ways to add a gift to your list:</p>
<h3>1. Form in the panel</h3>
<p>In the panel open your list and click <strong>"+ Add gift"</strong>. Type a title, price and optionally a shop link.</p>
<h3>2. Paste a shop link (auto-preview)</h3>
<p>In the open form paste a link from e.g. Allegro, Empik or x-kom. The "Title" and "Price" fields fill in automatically from the page's meta tags.</p>
<h3>3. Bookmarklet</h3>
<p>In the panel grab the <strong>"Add to DajPrezent.pl"</strong> bookmarklet and drag it onto your browser bookmarks bar. One click on any shop tab opens a window with a ready-to-add gift.</p>
<p>Each gift can have: title, description, price, shop link, image (JPG / PNG / WebP up to 4 MB — auto-converted to WebP).</p>

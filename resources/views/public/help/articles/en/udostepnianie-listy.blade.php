<p>In the panel, on a list card, click <strong>"Share list"</strong>. A section unfolds with:</p>
<ul>
    <li>📋 <strong>Copy link</strong> — ready URL to paste anywhere.</li>
    <li>💬 <strong>WhatsApp</strong> — instant share with prefilled text.</li>
    <li>📨 <strong>Messenger</strong> — Facebook share.</li>
    <li>✉️ <strong>Email</strong> — opens mail composer with text + link.</li>
    <li>⬇ <strong>QR (SVG)</strong> — download to print on an invitation or stick on the fridge.</li>
</ul>
<p>Each guest just opens your link — no account needed, nothing to install.</p>

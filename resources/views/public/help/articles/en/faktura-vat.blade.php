<p>Every plan purchase produces a VAT invoice automatically:</p>
<ol>
    <li>After payment (PayU IPN) an invoice is created with number <code>FV/YYYY/MM/####</code> (e.g. FV/2026/06/0001).</li>
    <li>The invoice is sent to <strong>KSeF</strong> (Polish Ministry of Finance e-Invoice System).</li>
    <li>You'll find the PDF in your panel under <strong>"Invoices"</strong>.</li>
</ol>
<h3>B2C invoice (individual)</h3>
<p>By default we issue a personal invoice — with your name and address from the checkout form.</p>
<h3>B2B invoice (company)</h3>
<p>In the checkout form tick <em>"Buying as a company"</em> and provide your Polish tax ID (NIP). We validate it with the official MF algorithm and issue the invoice with company details.</p>

<p>To reserve a gift from someone's list:</p>
<ol>
    <li>Open the link the host shared with you (e.g. <code>dajprezent.pl/anna-30</code>).</li>
    <li>Click <strong>"Reserve this gift"</strong> next to the item you want.</li>
    <li>Enter your email. You'll receive a confirmation link (anti-spam).</li>
    <li>Click the link in the email — done, the gift is now marked as reserved.</li>
</ol>
<p>The host never sees your email — we only ask for it so nobody can spam-reserve every gift on the list.</p>

<p>Trzy sposoby dodania prezentu do listy:</p>
<h3>1. Formularz w panelu</h3>
<p>W panelu otwórz Twoją listę i kliknij <strong>„+ Dodaj prezent"</strong>. Wpisz tytuł, cenę i opcjonalnie link do sklepu.</p>
<h3>2. Wklej link ze sklepu (autopodgląd)</h3>
<p>W otwartym formularzu wklej link np. z Allegro, Empiku czy x-kom. Pole „Tytuł" i „Cena" wypełnią się automatycznie z meta-tagów strony.</p>
<h3>3. Bookmarklet</h3>
<p>W panelu pobierz bookmarklet <strong>„Dodaj do DajPrezent.pl"</strong> i przeciągnij na pasek zakładek przeglądarki. Klik na karcie sklepu otworzy okienko z gotowym do dodania prezentem.</p>
<p>Każdy prezent może mieć: tytuł, opis, cenę, link do sklepu, zdjęcie (JPG/PNG/WebP do 4 MB — automatycznie konwertowane do WebP).</p>
